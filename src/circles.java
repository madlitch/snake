public class circles {
	int x;                               // Coordonné X
	int y;                               // Coordonné Y
	public circles(int sx, int sy){
		x = sx;                     // Constructeur de l'objet
		y = sy;}
	////////////////////////////// Accesseurs
	public int getX(){
		return x;}
	public int getY(){
		return y;}
	////////////////////////////// Mutateurs de coordonnées
	public void setX(int newx){ 
		x = newx;}
	public void setY(int newy){
		y = newy;}}


